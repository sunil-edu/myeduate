package main

import (
	"log"
	"myeduate/utils"

	"entgo.io/contrib/entgql"
	"entgo.io/ent/entc"
	"entgo.io/ent/entc/gen"
)

func main() {
	ex, err := entgql.NewExtension(
		//entgql.WithWhereFilters(true),
		entgql.WithConfigPath("../gqlgen.yml"),
		entgql.WithSchemaPath("../grpql/ent.graphql"),
	)
	if err != nil {
		log.Fatalf("creating entgql extension: %v", err)
	}
	opts := []entc.Option{
		entc.Extensions(ex),
		entc.TemplateDir("./template"),
		entc.FeatureNames("privacy"),
		entc.FeatureNames("sql/modifier"),
		entc.FeatureNames("entql"),
		entc.Dependency(
			entc.DependencyType(&utils.Config{}),
		),
	}
	if err := entc.Generate("./schema", &gen.Config{

		Header: `
			// Copyright 2021-present Eduate Pvt Ltd. All rights reserved.
			// Code generated by entc, DO NOT EDIT.
		`,
	}, opts...); err != nil {
		log.Fatalf("running ent codegen: %v", err)
	}
}
